export type Locale = "fr" | "en";

export function getLocale(value?: string): Locale {
  return value === "en" ? "en" : "fr";
}

export function withLocale(path: string, locale: Locale) {
  return locale === "fr" ? path : `${path}${path.includes("?") ? "&" : "?"}lang=en`;
}

